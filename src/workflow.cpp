std::string shell_quote(const std::filesystem::path& path) {
    std::string raw = path.string();
    std::string quoted = "'";
    for (const char ch : raw) {
        if (ch == '\'') {
            quoted += "'\\''";
        } else {
            quoted += ch;
        }
    }
    quoted += "'";
    return quoted;
}

std::string shell_quote(const std::string& value) {
    return shell_quote(std::filesystem::path(value));
}

std::string slugify(std::string value) {
    std::replace(value.begin(), value.end(), '/', '-');
    std::replace(value.begin(), value.end(), ':', '-');
    for (char& ch : value) {
        if (!std::isalnum(static_cast<unsigned char>(ch)) && ch != '-' && ch != '_') {
            ch = '-';
        }
        ch = static_cast<char>(std::tolower(static_cast<unsigned char>(ch)));
    }
    return value;
}

std::filesystem::path default_llama_cpp_dir(const WorkflowOptions& options) {
    if (!options.llama_cpp_dir.empty()) {
        return options.llama_cpp_dir;
    }
    if (const char* env = std::getenv("LLAMA_CPP_DIR")) {
        return env;
    }
    return "third_party/llama.cpp";
}

std::filesystem::path quantize_binary(const std::filesystem::path& llama_cpp_dir) {
    const std::array<std::filesystem::path, 5> candidates = {
        llama_cpp_dir / "build/bin/llama-quantize",
        llama_cpp_dir / "build/bin/quantize",
        llama_cpp_dir / "llama-quantize",
        llama_cpp_dir / "quantize",
        "llama-quantize",
    };
    for (const auto& candidate : candidates) {
        if (candidate == "llama-quantize" || std::filesystem::exists(candidate)) {
            return candidate;
        }
    }
    return candidates.front();
}

std::string normalize_token(std::string token) {
    token.erase(std::remove_if(token.begin(), token.end(),
                               [](unsigned char ch) { return !std::isalnum(ch); }),
                token.end());
    std::transform(token.begin(), token.end(), token.begin(),
                   [](unsigned char ch) { return static_cast<char>(std::tolower(ch)); });
    return token;
}

std::set<std::string> token_set(const std::string& text) {
    std::istringstream stream(text);
    std::set<std::string> tokens;
    std::string token;
    while (stream >> token) {
        token = normalize_token(token);
        if (token.size() > 2U) {
            tokens.insert(token);
        }
    }
    return tokens;
}

void write_file(const std::filesystem::path& path, const std::string& content) {
    std::filesystem::create_directories(path.parent_path());
    std::ofstream file(path);
    if (!file) {
        throw std::runtime_error("failed to write " + path.string());
    }
    file << content;
}

std::string modelfile_content(const WorkflowOptions& options, const std::filesystem::path& gguf_path) {
    std::ostringstream out;
    out << "FROM " << gguf_path.string() << "\n";
    out << "PARAMETER temperature 0.7\n";
    out << "PARAMETER top_p 0.9\n";
    out << "PARAMETER num_ctx 4096\n";
    out << "SYSTEM You are a locally running compressed model. Be accurate, concise, and transparent about uncertainty.\n";
    out << "# Generated by NanoQuant for " << options.model_id << "\n";
    return out.str();
}

std::string report_markdown(const WorkflowOptions& options,
                            const WorkflowPlan& plan,
                            const std::optional<ComparisonReport>& comparison) {
    std::ostringstream out;
    out << "# NanoQuant Run Report\n\n";
    out << "- model: `" << options.model_id << "`\n";
    out << "- quantization: `" << options.quantization << "`\n";
    out << "- ollama model: `" << options.ollama_name << "`\n";
    if (!options.base_ollama_name.empty()) {
        out << "- reference model: `" << options.base_ollama_name << "`\n";
    } else if (!options.reference_ollama_name.empty()) {
        out << "- reference model: `" << options.reference_ollama_name << "`\n";
    }
    out << "- pull command: `ollama pull " << options.ollama_name << "`\n";
    out << "- artifact: `" << plan.quantized_gguf.string() << "`\n\n";

    if (comparison.has_value()) {
        out << "## Comparison\n\n";
        out << "- lexical overlap: `" << comparison->lexical_overlap << "`\n";
        out << "- length ratio: `" << comparison->length_ratio << "`\n";
        out << "- likely degraded: `" << (comparison->likely_degraded ? "yes" : "no") << "`\n\n";
        out << "### Base Output\n\n```text\n" << comparison->base_output << "\n```\n\n";
        out << "### Compressed Output\n\n```text\n" << comparison->compressed_output << "\n```\n\n";
        if (comparison->likely_degraded) {
            out << build_tuning_recommendation(options, *comparison);
        }
    } else {
        out << "Comparison skipped. Pass `--base-ollama-name <name>` to compare against an existing Ollama model.\n";
    }

    return out.str();
}

